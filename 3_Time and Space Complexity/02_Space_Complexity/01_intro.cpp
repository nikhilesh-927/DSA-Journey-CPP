/*

    Space complexity ----> It determines the amount of memory an algorithm uses in terms of the size of the input. 

    Space Complexity often consists of two components:

    1. Input Space : Memory required to store the input. 
    2. Auxiliary Space : Extra space or temporary space used by an algorithm. 

    Auxiliary Space v/s Input Space: 
    
    Auxiliary space refers to the extra space used by the algorithm.
    Understanding this distinction is key to writing efficient code. 

*/